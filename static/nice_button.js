document.querySelector('#nice_button').onmouseover = function() {
    document.querySelector('#nice_button').style.backgroundColor = '#33332d'
    document.querySelector('#nice_button').style.color = 'white'
            }

document.querySelector('#nice_button').onmouseout = function() {
    document.querySelector('#nice_button').style.backgroundColor = 'initial'
    document.querySelector('#nice_button').style.color = 'initial'
            }